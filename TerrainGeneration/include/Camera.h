#ifndef CAMERA_H_
#define CAMERA_H_

#include <iostream>
#include <vector>
#include <ngl/Vec3.h>

//----------------------------------------------------------------------------------------------------------------------
/// @class Camera
/// @brief this class stores the details of the camera for NGLScene
/// @todo tidying up variable and method names
//----------------------------------------------------------------------------------------------------------------------


class Camera
{    
public:

  //CONSTRUCTOR
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief default ctor for Camera class
  //--------------------------------------------------------------------------------------------------------------------
  Camera() = default;

  //PUBLIC MEMBER VARIABLES
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief position vector of the camera
  //--------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_from = {0,-50,50};
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief position vector the camera is pointed at
  //--------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_to = {0,0,0};
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief the nominal up direction of the camera
  //--------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_up = {0,0,1};
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief the vector pointing to the right of the camera
  //--------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_right = (m_to-m_from).cross(m_up);
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief the actual up direction of the camera
  //--------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_trueUp = m_right.cross(m_to-m_from);

  //PUBLIC MEMBER FUNCTIONS
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief sets m_right and m_trueUp based on the other member variables
  //--------------------------------------------------------------------------------------------------------------------
  void update();
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief resets all member variables for the camera
  //--------------------------------------------------------------------------------------------------------------------
  void initialise();

};


#endif //CAMERA_H_
